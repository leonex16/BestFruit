CREATE OR REPLACE PROCEDURE SP_INSERTAR_IMAGEN(
    ID_IMG        IN   NUMBER,
    DESC_IMG    IN   VARCHAR2,
    EXTENSION_IMG IN   VARCHAR2
)
AS
    V_TEMP           BLOB;   --GUARDADO TEMPORAL DE LA IMAGEN 
    V_BFILE          BFILE; --GUARDA LA BUSQUEDA DE LA IMAGEN / VARIABLE QUE GUARDA CONTENIDO MULTIMEDIA / SRC
    V_NOMBRE_ARCHIVO VARCHAR2(100);
BEGIN  
    V_NOMBRE_ARCHIVO := CONCAT(CONCAT(DESC_IMG, '.'),  EXTENSION_IMG);
    INSERT INTO IMAGEN(ID_IMAGEN, DESC_IMAGEN, EXT_IMAGEN, IMAGEN)
        VALUES(ID_IMG, DESC_IMG, EXTENSION_IMG, EMPTY_BLOB())
            RETURNING IMAGEN INTO V_TEMP;
    V_BFILE := BFILENAME('DIR_TEMP', V_NOMBRE_ARCHIVO);
    DBMS_LOB.OPEN(V_BFILE, DBMS_LOB.LOB_READONLY);
    DBMS_LOB.LOADFROMFILE(V_TEMP, V_BFILE, SYS.DBMS_LOB.GETLENGTH(V_BFILE));
    DBMS_LOB.CLOSE(V_BFILE);
    COMMIT;

EXCEPTION 
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE(SQLERRM);
END SP_INSERTAR_IMAGEN;

